package lesson_13_Conditions;

public class ShortCutIf {
	
	public static void main(String[] args) {
		
		int i = 5;
		
		String s = (i <= 10)? "Yes" : "No";
		
		System.out.println(s);
		
		String name = "Pankaj";
		
		boolean result = (name.contains("QA"))? true : false;
		System.out.println(result);
		
		
	}
	
	
	

}
